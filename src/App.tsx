function App() {
    const [imageBase64, setImageBase64] = useState<string | null>(null);
    const [result, setResult] = useState<Record<string, any>>({});
    const canvasRef = useRef<HTMLCanvasElement>(null);

    const handleImageUpload = (event: React.ChangeEvent<HTMLInputElement>) => {
        const file = event.target.files?.[0];
        if (file) {
            const reader = new FileReader();
            reader.onloadend = () => {
                setImageBase64(reader.result as string);
            };
            reader.readAsDataURL(file);
        }
    };

    useEffect(() => {
        if (result && canvasRef.current) {
            const canvas = canvasRef.current;
            const ctx = canvas.getContext('2d');
            if (ctx) {
                const img = new Image();
                img.src = imageBase64;
                img.onload = () => {
                    canvas.width = result.image.width;
                    canvas.height = result.image.height;
                    ctx.drawImage(img, 0, 0, canvas.width, canvas.height);

                    result.predictions.forEach(prediction => {
                        ctx.strokeStyle = 'red';
                        ctx.lineWidth = 5;
                        ctx.beginPath();
                        ctx.rect(prediction.x, prediction.y, prediction.width, prediction.height);
                        ctx.stroke();

                        ctx.fillStyle = 'red';
                        ctx.font = '20px Arial';
                        ctx.fillText(prediction.class, prediction.x, prediction.y - 10);

                        ctx.beginPath();
                        ctx.moveTo(prediction.points[0].x, prediction.points[0].y);
                        prediction.points.forEach(point => {
                            ctx.lineTo(point.x, point.y);
                        });
                        ctx.closePath();
                        ctx.stroke();
                    });
                };
            }
        }
    }, [imageBase64, result]);

    const handleSendImg =  () => {
        axios.default({
            method: "POST",
            url: "https://outline.roboflow.com/palm-reading-b3tep/1",
            params: {
                api_key: "21FncoC7INRX0lCo2YpZ"
            },
            data: imageBase64,
            headers: {
                "Content-Type": "application/x-www-form-urlencoded"
            }
        })
            .then(function (response) {
                console.log(response.data);
                setResult(response.data);
            })
            .catch(function (error) {
                console.log(error.message);
            });
    }

    return (
        <div>
            <h1>Upload Image</h1>
            <input type="file" accept="image/*" onChange={handleImageUpload} />
            {imageBase64 && (
                <div>
                    <button onClick={handleSendImg}>Send</button>
                    <h2>Preview:</h2>
                </div>
            )}
            <canvas ref={canvasRef} style={{ maxWidth: '100%' }} />
        </div>
    );
}

export default App
